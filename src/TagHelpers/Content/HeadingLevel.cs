namespace TagHelpers.Content;

/// <summary>
/// HTML defines six levels of headings, and these heading elements are H1, H2, H3, H4, H5, and H6.
/// The H1 element is the highest or most important level, and the H6 element is the least important.
/// </summary>
public enum HeadingLevel
{
    H1 = 1,
    H2 = 2,
    H3 = 3,
    H4 = 4,
    H5 = 5,
    H6 = 6,
}
